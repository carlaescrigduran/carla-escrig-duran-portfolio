const langToggle = document.getElementById("langToggle");
const themeToggle = document.getElementById("themeToggle");
const timelineRange = document.getElementById("timelineRange");
const timelineYear = document.getElementById("timelineYear");
const timelineDetail = document.getElementById("timelineDetail");
const currentYear = document.getElementById("currentYear");
const modal = document.getElementById("projectModal");
const modalTitle = document.getElementById("modalTitle");
const modalText = document.getElementById("modalText");
const modalClose = document.getElementById("modalClose");
const contactForm = document.getElementById("contactForm");
const nameInput = document.getElementById("name");
const formStatus = document.getElementById("formStatus");

let currentLang = localStorage.getItem("lang") || "en";
const englishOverrides = {};

function captureEnglishOverrides() {
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    const htmlText = node.textContent.trim();
    const defaultText = translations.en[key];

    // If HTML was edited manually, keep that as the English source of truth.
    if (key && htmlText && defaultText && htmlText !== defaultText) {
      englishOverrides[key] = htmlText;
    }
  });
}

function t(key) {
  if (currentLang === "en" && englishOverrides[key]) {
    return englishOverrides[key];
  }

  return translations[currentLang][key] || translations.en[key] || key;
}

function applyTranslations() {
  document.documentElement.lang = currentLang;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    node.textContent = t(key);
  });

  langToggle.textContent = currentLang === "en" ? "ES" : "EN";
  updateTimeline();

  if (formStatus && !formStatus.hidden && formStatus.dataset.name) {
    formStatus.textContent = t("form_success").replace("{name}", formStatus.dataset.name);
  }
}

function getTimelineText(lang, year) {
  const entries = timelineEntries[lang];
  if (entries[year]) {
    return entries[year];
  }

  const nearest = Object.keys(entries)
    .map(Number)
    .sort((a, b) => Math.abs(a - year) - Math.abs(b - year))[0];

  return entries[nearest];
}

function updateTimeline() {
  const year = Number(timelineRange.value);
  timelineYear.textContent = String(year);
  timelineDetail.textContent = getTimelineText(currentLang, year);
}

function applyTheme() {
  const storedTheme = localStorage.getItem("theme") || "light";
  document.body.classList.toggle("dark", storedTheme === "dark");
  themeToggle.textContent = storedTheme === "dark" ? "Light" : "Dark";
}

function openProjectModal(projectKey) {
  modalTitle.textContent = t(`modal_${projectKey}_title`);
  modalText.textContent = t(`modal_${projectKey}_text`);
  modal.showModal();
}

langToggle.addEventListener("click", () => {
  currentLang = currentLang === "en" ? "es" : "en";
  localStorage.setItem("lang", currentLang);
  applyTranslations();
});

themeToggle.addEventListener("click", () => {
  const nextTheme = document.body.classList.contains("dark") ? "light" : "dark";
  localStorage.setItem("theme", nextTheme);
  applyTheme();
});

timelineRange.addEventListener("input", updateTimeline);

document.querySelectorAll(".modal-open").forEach((button) => {
  button.addEventListener("click", () => {
    openProjectModal(button.dataset.project);
  });
});

modalClose.addEventListener("click", () => modal.close());

modal.addEventListener("click", (event) => {
  if (event.target === modal) {
    modal.close();
  }
});

if (contactForm && nameInput && formStatus) {
  contactForm.addEventListener("submit", (event) => {
    if (!contactForm.checkValidity()) {
      contactForm.reportValidity();
      return;
    }

    event.preventDefault();
    const submittedName = nameInput.value.trim();
    const fallbackName = currentLang === "es" ? "artista" : "artist";
    const finalName = submittedName || fallbackName;

    formStatus.dataset.name = finalName;
    formStatus.textContent = t("form_success").replace("{name}", finalName);
    formStatus.hidden = false;

    contactForm.reset();
  });
}

currentYear.textContent = new Date().getFullYear();
applyTheme();
captureEnglishOverrides();
applyTranslations();
